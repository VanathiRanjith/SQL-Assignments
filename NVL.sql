SELECT EMPLOYEE_ID, SALARY, COMMISSION_PCT ,

NVL(COMMISSION_PCT,10)*(20/100) " COMMIS",

NVL(COMMISSION_PCT,0)+20 "COMMISION+BONUS"

FROM EMPLOYEES;


SELECT EMPLOYEE_ID, FIRST_NAME,lAST_NAME,COMMISSION_PCT,

NVL2(COMMISSION_PCT,('COMMISSION_PCT*20'),'ALLOCATE_BONUS')"STATUS"


FROM EMPLOYEES

WHERE DEPARTMENT_ID IN ('60','80');







SELECT CITY,LENGTH(CITY)"EXPR1",

STATE_PROVINCE,NVL(LENGTH(STATE_PROVINCE),0) "EXPR2",

NULLIF(LENGTH (CITY),LENGTH(STATE_PROVINCE))"RESULT"

FROM LOCATIONS;-- error beORA-00909: invalid number of arguments


SELECT ROWNUM,(CITY),LENGTH(CITY)"EXPR1",

STATE_PROVINCE,NVL(LENGTH(STATE_PROVINCE),0) "EXPR2",

COALESCE (NULLIF(TO_CHAR (LENGTH (CITY)) , TO_CHAR(LENGTH(STATE_PROVINCE))),'BOTH SAME LENGTH')"RESULT"

FROM LOCATIONS;



































































