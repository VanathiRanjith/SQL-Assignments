SELECT EMPLOYEE_ID,FIRST_NAME,JOB_ID,SALARY,

CASE JOB_ID

WHEN 'AD_PRES' THEN SALARY+(SALARY*(20/100))

WHEN 'AD_VP' THEN SALARY+(SALARY*(15/100))

ELSE SALARY

END "REVISED SALARY"

FROM EMPLOYEES;


